let color = 'red';

switch (color) {
  case "red":
    console.log("This is red");
    break;

  case "green":
    console.log("This is green");
    break;

  case "blue":
    console.log("This is blue");
    break;

  case "yellow":
    console.log("This is yellow");
    break;

  default:
    console.log("This is default");
}
